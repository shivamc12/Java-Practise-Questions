public class Matrix_Transpose2order {
    public static void main(String[]args)
    {
        int[][] arr1={{1,2},{4,5}};
        int[][] arr2={{3,5},{5,6}};
        

        for(int i=0;i<2;i++)
        {
            for(int j=0;j<2;j++)
            {
                
                System.out.println(arr1[i][j]+arr2[i][j]);
            }
        }

    }
    
}
